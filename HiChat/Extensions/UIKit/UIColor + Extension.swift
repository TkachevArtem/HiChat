import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return UIColor(red: 208, green: 2, blue: 27, alpha: 1)
    }
    
    static func buttonWhite() -> UIColor {
        return UIColor(red: 247, green: 248, blue: 253, alpha: 1)
    }
    
    static func buttonBlack() -> UIColor {
        return UIColor(red: 51, green: 51, blue: 51, alpha: 1)
    }
    
}
